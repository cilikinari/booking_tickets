class Movie {
  final String title;
  final String imagePath;
  final String genre;
  final String duration;
  final String year;
  final String ageRating;
  final double price;
  final String description;

  Movie({
    required this.title,
    required this.imagePath,
    required this.genre,
    required this.duration,
    required this.year,
    required this.ageRating,
    required this.price,
    required this.description,
  });
}